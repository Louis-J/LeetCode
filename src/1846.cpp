class Solution {
public:
    int maximumElementAfterDecrementingAndRearranging1(vector<int> &arr) {
        int         minusNum = 0;
        int         minusMin = 1;
        vector<int> newArr;

        for(auto i : arr) {
            if(i < 1) {
                minusNum++;
                if(i < minusMin) minusMin = i;
            } else
                newArr.emplace_back(i);
        }

        sort(newArr.begin(), newArr.end());
        int posi = max((1 - minusMin) - minusNum, 0);
        int off  = 0;

        newArr[posi] = 1;
        for(int i = 1 + posi; i < newArr.size(); i++) {
            if(newArr[i] - newArr[i - 1] > 1) {
                off += newArr[i] - newArr[i - 1] - 1;
            } else if(off > 0 && newArr[i] == newArr[i - 1]) {
                off--;
            }
        }
        return newArr[newArr.size() - 1] - off;
    }

    int maximumElementAfterDecrementingAndRearranging2(vector<int> &arr) {
        int minusMin = 1;
        int length   = 0;
        int newArr[arr.size()];

        for(auto i : arr) {
            if(i < 1) {
                if(i < minusMin) minusMin = i;
            } else
                newArr[length++] = i;
        }

        sort(newArr, newArr + length);
        int minusNum = arr.size() - length;
        int posi     = max((1 - minusMin) - minusNum, 0);
        int off      = 0;

        newArr[posi] = 1;
        for(int i = 1 + posi; i < length; i++) {
            if(newArr[i] - newArr[i - 1] > 1) {
                off += newArr[i] - newArr[i - 1] - 1;
            } else if(off > 0 && newArr[i] == newArr[i - 1]) {
                off--;
            }
        }
        return newArr[length - 1] - off;
    }

    int maximumElementAfterDecrementingAndRearranging3(vector<int> &arr) {
        int minusNum = 0;
        int minusMin = 1;

        for(auto i : arr) {
            if(i < 1) {
                minusNum++;
                if(i < minusMin) minusMin = i;
            }
        }

        sort(arr.begin(), arr.end());
        int posi = minusNum + max((1 - minusMin) - minusNum, 0);
        int off  = 0;

        arr[posi] = 1;
        for(int i = 1 + posi; i < arr.size(); i++) {
            if(arr[i] - arr[i - 1] > 1) {
                off += arr[i] - arr[i - 1] - 1;
            } else if(off > 0 && arr[i] == arr[i - 1]) {
                off--;
            }
        }
        return arr[arr.size() - 1] - off;
    }

    int maximumElementAfterDecrementingAndRearranging(vector<int> &arr) {
        int minusMin = 1;
        int length   = 0;
        int newArr[arr.size()];

        for(auto i : arr) {
            if(i < 1) {
                if(i < minusMin) minusMin = i;
            } else
                newArr[length++] = i;
        }

        sort(newArr, newArr + length);
        int minusNum = arr.size() - length;
        int posi     = max((1 - minusMin) - minusNum, 0);

        newArr[posi] = 1;
        for(int i = 1 + posi; i < length; i++) {
            if(newArr[i] - newArr[i - 1] > 1) newArr[i] = newArr[i - 1] + 1;
        }
        return newArr[length - 1];
    }
};

